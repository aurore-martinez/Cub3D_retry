#include "../include/cub3d.h"

void	print_map(char **map)
{
	int	y;
	int	x;

	y = 0;
	while (map[y])
	{
		x = 0;
		while (map[y][x])
		{
			printf("%c|", map[y][x]);
			x++;
		}
		printf("\n");
		y++;
	}
}
